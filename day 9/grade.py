g = int(input('enter your marks: '))

if 100 >= g >= 90 :
    print('you have an A')
elif 90 > g >= 80 :
    print('you have a b')
elif 80 > g >= 70 :
    print('you have a C')
elif 70 > g >= 60 :
    print('you have a D')
elif 60 > g >= 50 :
    print('you have an E')
else: 
    print('you have failed')